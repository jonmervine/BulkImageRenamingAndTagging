public class IqdbException extends Exception {

    public IqdbException() {
    }

    public IqdbException(String message) {
        super(message);
    }

    public IqdbException(Throwable cause) {
        super(cause);
    }

    public IqdbException(String message, Throwable cause) {
        super(message, cause);
    }
}
